//Artithmetic

var add = function(num1, num2) {
    return num1 + num2;
  };
  
  var subtract = function(num1, num2) {
    return num1 - num2;
  };
  
  var multiply = function(num1, num2) {
    return num1 * num2;
  };
  
  var divide = function(num1, num2) {
    return num1 / num2;
  };
  

//Odd or Even
var oddOrEven = function(num) {
  if (num % 2 === 0) {
    return "even";
  } else {
    return "odd";
  }
};



